package kr.or.ddit.study03;

public class ArithmeticExample {
	public static void main(String[] args) {
		/*
		 * 산술연산자
		 * + , - , / , * , %
		 * 사칙연산과 마찬가지로 /,*우선적용
		 * 
		 */

		int a = 7;
		int b = 5;
		System.out.println("덧샘 = " + (a + b));
		System.out.println("뺼샘 = " + (a - b));
		System.out.println("나눗샘 = " + (a / b));
		System.out.println("곱샘 = " + (a * b));
		System.out.println("나머지 = " + (a % b));
		
	}

}
