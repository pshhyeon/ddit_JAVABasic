package kr.or.ddit.study02.sec01;

public class VarExample05 {
	public static void main(String[] args) {
		/*
		 * 변수 이름 규칙
		 * 1. 첫번째 글자는 문자이거나 $, _ 이어야 하고 숫자로 시작 X
		 * 2. 영어 대소문자를 구분
		 * 3. 첫 문자는 소문자 중간 단어는 대문자(필수는 아님)
		 * 4. 변수 길이는 제한 없음
		 * 5. 예약어는 사용불가
		 * 
		 */
		
		/*
		 * 1
		 */
		int $a;
		int _a;
		// int 1a; 사용불가
		
		/*
		 * 2
		 */
		int a;
		int A;
		
		/*
		 * 3
		 */
		int maxSpeed;
		int sellPrice;
		
		/*
		 * 4
		 */
		int aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa;
		
		/*
		 * 5
		 */
		// int int; 예약어 사용불가.
		
	}
}
